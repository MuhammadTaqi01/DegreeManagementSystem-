    private void jBtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDeleteActionPerformed
        String ID = jTexID.getText();
        DataRecords DR = new DataRecords();
        try{
        DR.deleteStudentRecord(ID);
        DR.deleteDegreeFormRecord(ID);
        DR.deleteTranscriptRecord(ID);
        DR.deleteDegreeRecord(ID);
        DR.deleteFirstSemesterMarks(ID);
        DR.deleteSecondSemesterMarks(ID);
        JOptionPane.showMessageDialog(this, "Delete Successfully!");
        }
        catch(Exception e){
            System.out.println(e);
        }
        Clear();
    }//GEN-LAST:event_jBtnDeleteActionPerformed

