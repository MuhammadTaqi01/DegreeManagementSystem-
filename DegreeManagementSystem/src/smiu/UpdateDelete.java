    private void jBtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSearchActionPerformed
        String ID = jTexID.getText();
        DataRecords DR = new DataRecords();
        ResultSet rs = DR.searchStudentRecord(ID);
        try{
            while(rs.next()){
                String data[] = {rs.getString("Name"),rs.getString("Father"),rs.getString("Password"),rs.getString("PhoneNo"),rs.getString("CNIC"),rs.getString("Email"),rs.getString("Department"),rs.getString("Batch")};
                jTexName.setText(data[0]);
                jTexFather.setText(data[1]);
                jTexPassword.setText(data[2]);
                jTexPhoneNo.setText(data[3]);
                jTexCNIC.setText(data[4]);
                jTexEmail.setText(data[5]);
                jTexDepartment.setText(data[6]);
            } 
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jBtnSearchActionPerformed

    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        String ID = jTexID.getText();
        String Name = jTexName.getText();
        String Father = jTexFather.getText();
        String Password = jTexPassword.getText();
        String PhoneNo = jTexPhoneNo.getText();
        String CNIC = jTexCNIC.getText();
        String Email = jTexEmail.getText();
        String Department = jTexDepartment.getText();
        DataRecords DR = new DataRecords();
        try{
        DR.updateStudentRecords(ID, Name,Father, Password, PhoneNo, CNIC, Email, Department);
        JOptionPane.showMessageDialog(this, "Update Successfully!");
        }
        catch(Exception e){
            System.out.println(e);
        }
        Clear();
    }//GEN-LAST:event_jBtnUpdateActionPerformed

